#include <stdio.h>

/* Simple program that prints messages. Try using redirection
   to change where the output goes!
 */
int main() {
    // Prints to standard output (your screen, by default):
    printf("Where's my text?\n");

    // Prints to standard error instead of standard output:
    fprintf(stderr, "Printing some stuff on standard error\n");

    return 0;
}






















